				<h2>1937. We opened our doors on July 13, 1937</h2>
				<h3>A <i>Lucky</i> HAND</h3>
				<p><b>Vernon Rudolph</b> bought a secret yeast-raised doughnut recipe from a French chef, rented a building in what is now historic Old Harlem in Bedford Square, London. But Legend has it that a young Vernon Rudolph, the man destined to be the <b>‘King of Creamy Doughnut'</b> won the secret yeast raised doughnut recipe in a poker game with the French Chef. We’re not sure ourselves, but it sure makes a great story.</p>
				<h3>A <i>Hole in</i> THE WALL</h3>
				<p>He began selling <b>Vanilla Cream topped doughnuts</b> on July 13, 1937 to local grocery stores. The delicious scent of cooking doughnuts drifted into the streets, and passers-by stopped to ask if they could buy hot doughnuts. So, he cut a hole in an outside wall and started selling hot <b>Original Creamy&reg; doughnuts</b> directly to customers on the sidewalk.</p>
