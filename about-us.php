				<p>In 1982, a small group of early franchisees bought Creamy Doughnut back from Beatrice Foods. A renewed focused on the hot doughnut experience became a priority for the company. Creamy Doughnut began to expand outside the City Of London and opened its first store in <b>Liverpool in 1996</b>. Soon afterwards, in 1999, the first store in Manchester opened and national expansion was well underway. When Creamy Doughnut turned 60 years old in 1997, it was officially recognized as a 20th century English icon.</p>&nbsp;
