				<h2>1940s-50s. Improving the doughnut making process through innovations</h2>
				<p>By now there was a small chain of stores, mostly family-owned. They all used the Creamy Doughnut's recipe, but each store made its doughnuts from scratch. For Rudolph and Creamy Doughnut, the results were always good, but not consistent enough. So Creamy Doughnut built a mix plant and developed a distribution system that delivered the perfect dry doughnut mix to each Creamy Doughnut store. Then Rudolph and his equipment engineers invented and built Creamy Doughnut’s own <b>doughnut-making</b> equipment. From the 1950s on, they focused on improving and automating the doughnut-making process.</p>&nbsp;
				<h2>1960s-70s. Our stores are familiar gathering places for friends.</h2>
				<h3>DESIGN <i>of an</i> ICON</h3>
				<p>During the 1960s, Creamy Doughnut enjoyed steady growth throughout the Southeast and began expanding outside its traditional roots. The design of Creamy Doughnut stores became consistent including the <b>hallmark brown tile roof</b> and <b>heritage road signs</b>. Vernon Rudolph, our founder, died in 1973, and growth slowed as the company was reorganized for sale to Beatrice Foods Company in 1976.</p>
